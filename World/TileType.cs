/// <summary>
/// Типы тайлов карты.
/// Atlas coords в TileSet:
///   Grass     = (0,0)  #5A8C28 — строить можно, дорогу можно
///   Road      = (1,0)  #A08050 — дорога (не строить)
///   Water     = (2,0)  #2870D0 — нельзя строить/дорогу
///   Sand      = (3,0)  #D4B87A — пляж, строить можно, дорогу можно
///   Forest    = (4,0)  #2D5C10 — нельзя строить, нужен для Лесопилки
///   Rock      = (5,0)  #787878 — нельзя строить, нужен для Каменотёса
///   CopperOre = (6,0)  #B85A20 — медная руда, нельзя строить, нужна для Медной шахты
///   TinOre    = (7,0)  #8888AA — оловянная руда, нельзя строить, нужна для Оловянной шахты
///   Canal     = (8,0)  #2870D0 с коричневой рамкой — ирригационный канал
///   Desert    = (9,0)  #E8D08A — пустыня (внутренняя), строить можно, дорогу можно
/// </summary>
public enum TileType
{
    Grass     = 0,
    Road      = 1,
    Water     = 2,
    Sand      = 3,
    Forest    = 4,
    Rock      = 5,
    CopperOre = 6,
    TinOre    = 7,
    Canal     = 8,
    Desert    = 9,
}
